export const allResources: Resource[] = resourcesSeed.map((resource) => ({
  ...resource,
  market: resource.market ?? "global",
  category: categories.some((category) => category.slug === resource.category)
    ? resource.category
    : "resources",
}));

export const resources: Resource[] = allResources.filter((resource) => (
  resource.visibility !== "hidden" && resource.market === "cn"
));

export function getResource(slug: string) {
  return resources.find((resource) => resource.slug === slug);
}

export function getResourceOutboundUrl(resource: Resource) {
  return resource.affiliateUrl || resource.affiliateLinks?.[0]?.url || resource.url;
}

export function getAffiliateLabel(resource: Resource) {
  if (resource.affiliateStatus === "connected") {
    return resource.promotionLabel ?? "已接入";
  }

  if (resource.affiliateStatus === "available") {
    return resource.promotionLabel ?? "可申请";
  }

  if (resource.affiliateStatus === "pending") {
    return resource.promotionLabel ?? "待申请";
  }

  return null;
}

export function getResourcesByCategory(categorySlug: string) {
  return resources.filter((resource) => resource.category === categorySlug);
}

export function getFeaturedResources() {
  return resources.filter((resource) => resource.isFeatured).slice(0, 18);
}

export function getRankingResources() {
  return [...resources].sort((a, b) => {
    if (a.isSponsored !== b.isSponsored) {
      return Number(b.isSponsored) - Number(a.isSponsored);
    }
    if (a.isFeatured !== b.isFeatured) {
      return Number(b.isFeatured) - Number(a.isFeatured);
    }
    return a.name.localeCompare(b.name);
  });
}
